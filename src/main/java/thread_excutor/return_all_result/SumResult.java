package thread_excutor.return_all_result;

public class SumResult {

    private final String name;

    private final int value;

    public SumResult(String name, int value) {
        this.name = name;
        this.value = value;
    }

    public String getName() {
        return name;
    }

    public int getValue() {
        return value;
    }
}
